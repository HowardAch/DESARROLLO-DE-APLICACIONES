    private void btnOrdenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrdenarActionPerformed
        try {
        String[] lines = txtEntrada.getText().split("\n");
        ArrayList<Double> sortedList = new ArrayList<>();
        for (String line : lines) {
            sortedList.add(Double.parseDouble(line.trim()));
        }
        Collections.sort(sortedList);
        StringBuilder sortedText = new StringBuilder();
        for (Double number : sortedList) {
            sortedText.append(String.format("%.0f", number)).append("\n");
        }
        txtEntrada.setText(sortedText.toString());
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "No hay datos para ordenar.");
    }
    }//GEN-LAST:event_btnOrdenarActionPerformed

