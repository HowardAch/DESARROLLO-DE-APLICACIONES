    private void btnAgregarDatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarDatoActionPerformed
      try {
        String datoString = txtDato.getText().trim();
        if (!datoString.isEmpty()) {
            double dato = Double.parseDouble(datoString);
            operaciones.agregarDato(dato);
            txtEntrada.append(datoString + "\n");
            txtDato.setText("");
            txtDato.requestFocus();
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Por favor, ingrese un número válido.");
    }
    }//GEN-LAST:event_btnAgregarDatoActionPerformed

    private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed
       try {
        String[] lines = txtEntrada.getText().split("\n");
        if (lines.length > 0) {
            double dato = Double.parseDouble(lines[0].trim());
            operaciones.quitarDato(dato);
            StringBuilder updatedText = new StringBuilder();
            for (int i = 1; i < lines.length; i++) {
                updatedText.append(lines[i]).append("\n");
            }
            txtEntrada.setText(updatedText.toString().trim());
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "No hay datos para quitar.");
    }
    }//GEN-LAST:event_btnQuitarActionPerformed

